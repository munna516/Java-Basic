package Basic_Java;

public class AssignmentOperator {
    public static void main(String[] args) {
        int x;
        x = 8; // Assigning Value
        System.out.println(x);
        x += 5;  // x = x + 5;
        System.out.println(x);
        x -= 3;
        System.out.println(x);
        x *= 10;
        System.out.println(x);
        x /= 2;
        System.out.println(x);
        x %= 3;
        System.out.println(x);
    }
}
