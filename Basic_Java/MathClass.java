package Basic_Java;

public class MathClass {
    public static void main(String[] args) {
        int x = 5;
        int y = -6;
        System.out.println("Maxmimun : " + Math.max(x, y));
        System.out.println("Minimum : " + Math.min(x, y));
        System.out.println("Absolute Value : " + Math.abs(y));
        System.out.println("Power : " + Math.pow(2, 3));
        System.out.println("Round Value : " + Math.round(2.567));
        System.out.println("Floor : " + Math.floor(2.987));
        System.out.println("Ceil : " + Math.ceil(3.121));
        System.out.println("Pi Value  : " + Math.PI);
        System.out.println("Exponeent : " + Math.exp(5));
        System.out.println("Square : " + Math.sqrt(49));

    }
}
