package Basic_Java;

public class RelationalOperator {
    public static void main(String[] args) {
        int x = 7, y = 8;
        // All This are Relational operator
        System.out.println(x == y);
        System.out.println(x != y);
        System.out.println(x > y);
        System.out.println(x >= y);
        System.out.println(x < y);
        System.out.println(x <= y);
    }
}
